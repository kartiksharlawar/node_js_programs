/*Problem 6:
Problem Statement: Write a function printArrayElements that takes an array as input 
and prints each element of the array.
Example: printArrayElements([1, 2, 3, 4, 5]) should print: 1 2 3 4 5.

*/

function printArrayElements(arr) {
    for (let i = 0; i < arr.length; i++) {
        console.log(arr[i]);
    }
}

console.log(printArrayElements([1, 2, 3, 4, 5]))
  

